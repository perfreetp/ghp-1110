function generateDailyRecords() {
  const records = [];
  const today = new Date();
  for (let i = 29; i >= 0; i--) {
    const d = new Date(today);
    d.setDate(d.getDate() - i);
    records.push({
      date: `${d.getMonth() + 1}/${d.getDate()}`,
      tasks: Math.floor(Math.random() * 5) + 3,
      distance: Math.floor(Math.random() * 4000) + 2000,
      reports: Math.floor(Math.random() * 4),
    });
  }
  return records;
}

