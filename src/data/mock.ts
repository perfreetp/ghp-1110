function generateTrackPoints(baseLat: number, baseLng: number, count: number) {
  const points = [];
  let lat = baseLat;
  let lng = baseLng;
  const startTime = new Date('2026-06-10T08:30:00');
  for (let i = 0; i < count; i++) {
    lat += (Math.random() - 0.5) * 0.0008;
    lng += (Math.random() - 0.3) * 0.001;
    const t = new Date(startTime.getTime() + i * 120000);
    points.push({
      lat,
      lng,
      time: t.toISOString(),
      speed: Math.random() * 2 + 0.5,
    });
  }
  return points;
}

